package Inheritance.multilevel;

public class calculator {
    public float mul(float p, float q){
        return p*q;
    }

    public float subtraction(float p, float q){
        return p-q;
    }

    public float sum(float p, float q){
        return p+q;
    }

    public float div(float p, float q)
    {
        return p/q;
    }
}

